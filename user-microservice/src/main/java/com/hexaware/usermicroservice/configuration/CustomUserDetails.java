package com.hexaware.usermicroservice.configuration;

import java.util.Arrays;
import java.util.Collection;
import java.util.List;
import java.util.stream.Collectors;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import com.hexaware.usermicroservice.entity.Credential;

public class CustomUserDetails implements UserDetails {

	private Credential credential;
	private List<GrantedAuthority> authorities;

	public CustomUserDetails(Credential credential) {
		this.credential = credential;
		authorities = Arrays.stream(credential.getUser().getRole().split(",")).map(SimpleGrantedAuthority::new)
				.collect(Collectors.toList());
	}

	@Override
	public Collection<? extends GrantedAuthority> getAuthorities() {
		return authorities;
	}

	@Override
	public String getPassword() {
		return credential.getPassword();
	}

	@Override
	public String getUsername() {
		return credential.getUserName();
	}

	@Override
	public boolean isAccountNonExpired() {
		return true;
	}

	@Override
	public boolean isAccountNonLocked() {
		return true;
	}

	@Override
	public boolean isCredentialsNonExpired() {
		return true;
	}

	@Override
	public boolean isEnabled() {
		return true;
	}
}
